a = [10,20,30,40,10]

b = [75,65,35,75,30]

n = len(a)

m = len(b)

if a[0] == a[n-1]:
    print('a = [10,20,30,40,10] result is True')
else:
    print('a = [10,20,30,40,10] result is False')
if b[0] == b[m-1]:
    print('b = [75,65,35,75,30] result is True')
else:
    print('b = [75,65,35,75,30] result is False')